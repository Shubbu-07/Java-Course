public class practice9 {
    public static void main(String[] args) {
        // loops - while, do-while, for

        int i = 1;
        while (i <= 5) {
            System.out.println("Hi" + " " + "-" + " " + i);

            int j = 1;
            while (j <= 3) {
                System.out.println("Hello");
                j++;
            }

            i++;
        }
    }
}
