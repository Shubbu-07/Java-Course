public class practice2 {
    public static void main(String[] args) {
        int num1 = 7;
        int num2 = 5;
        int result = num1 + num2;
        int result2 = num1 / num2;
        System.out.println(result);
        System.out.println(result2);

        num1++; // post increment
        System.out.println(num1);

        // ++num1; pre increment
        // --num2; pre decrement

        num2--; // post decrement
        System.out.println(num2);
    }

}
